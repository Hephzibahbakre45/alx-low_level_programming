#include <stdio.h>
/**
 * Main - check the code
 * add - functiom that add the integer
 * sub - function that subtract the integer
 * div - function that divide the integer
 * mul - function that multiply the integer
 * @mod - function that modulate the integer
 * @a - first integer
 * @b - second integer
 * Return: Always EXIT_SUCCESS.
 */

int add(int a, int b)
{
	return (a + b);
}

int sub(int a, int b)
{
	return (a - b);
}

int div(int a, int b)
{
	return (a / b);
}

int mul(int a, int b)
{
	return (a * b);
}

int mod(int a, int b)
{
	return (a % b);
}
